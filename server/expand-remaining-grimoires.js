async function expandRemainingGrimoires() {
  console.log('📚 Expandindo os dois grimórios restantes para densidade uniforme...\n');
  
  for (const grimoireId of Object.keys(remainingContent)) {
    const chapters = remainingContent[grimoireId];
    
    console.log(`🔥 Expandindo grimório ID ${grimoireId}...`);
    
    for (const chapter of chapters) {
      const { error } = await supabase
        .from('chapters')
        .update({
          content: chapter.content
        })
        .eq('grimoire_id', parseInt(grimoireId))
        .eq('chapter_number', chapter.chapter_number);
        
      if (error) {
        console.error(`❌ Erro ao atualizar capítulo ${chapter.chapter_number}:`, error);
      } else {
        console.log(`  ✓ Capítulo ${chapter.chapter_number} expandido (${chapter.content.length} chars)`);
      }
    }
    
    console.log(`✅ Grimório ${grimoireId} expandido\n`);
  }
  
  console.log('🎉 TODOS os grimórios agora têm densidade uniforme!');
  console.log('📖 Paginação consistente em toda a coleção "Atrium Ignis"');
}

